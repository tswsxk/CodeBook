class Solution:
    def merge(self, nums1, m, nums2, n):
        """
        :type nums1: List[int]
        :type m: int
        :type nums2: List[int]
        :type n: int
        :rtype: void Do not return anything, modify nums1 in-place instead.
        """
        if n == 0:
            return
        nums1_idx = m - 1
        nums2_idx = n - 1
        insert_idx = m + n - 1
        while insert_idx >= 0 and nums1_idx >= 0 and nums2_idx >= 0:
            if nums1[nums1_idx] >= nums2[nums2_idx]:
                nums1[insert_idx] = nums1[nums1_idx]
                nums1_idx -= 1
            else:
                nums1[insert_idx] = nums2[nums2_idx]
                nums2_idx -= 1
            insert_idx -= 1
        while nums2_idx >= 0:
            nums1[insert_idx] = nums2[nums2_idx]
            nums2_idx -= 1
            insert_idx -= 1

